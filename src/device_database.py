def get_device_info(model_code: str) -> dict:
    """
    Ambil informasi device berdasarkan model code
    Return: dict dengan spesifikasi device
    """
    if model_code in SAMSUNG_DEVICES:
        return SAMSUNG_DEVICES[model_code]
    return None


def get_device_by_usb_id(vid: int, pid: int) -> tuple:
    """
    Identifikasi device berdasarkan USB Vendor ID dan Product ID
    Return: (model_code, device_info)
    """
    key = (vid, pid)
    if key in SAMSUNG_USB_IDS:
        model_code = SAMSUNG_USB_IDS[key]
        if model_code != "unknown":
            return model_code, SAMSUNG_DEVICES.get(model_code, None)
    return None, None


def get_vendor_name(vid: int) -> str:
    """
    Ambil nama vendor berdasarkan Vendor ID
    """
    if vid in ANDROID_VENDOR_IDS:
        return ANDROID_VENDOR_IDS[vid]["name"]
    return f"Unknown (0x{vid:04X})"


def calculate_keypad_coords(screen_width: int, screen_height: int) -> dict:
    """
    Hitung koordinat keypad berdasarkan resolusi layar
    Normalisasi ke 0-10000 (HID standard)
    
    Layout Samsung PIN Keypad:
    ┌─────────────────────┐
    │     1   2   3       │
    │     4   5   6       │
    │     7   8   9       │
    │   DEL 0   (empty)   │
    └─────────────────────┘
    """
    # Normalisasi ke 0-10000
    # Keypad berada di bagian bawah layar (sekitar 55%-90% dari tinggi)
    y_start = int(0.55 * 10000)  # 5500 - mulai keypad
    y_end = int(0.90 * 10000)    # 9000 - akhir keypad
    
    # X positions: 3 kolom (kiri, tengah, kanan)
    x_left = int(0.17 * 10000)   # 1700
    x_center = int(0.50 * 10000) # 5000
    x_right = int(0.83 * 10000)  # 8300
    
    # Y positions: 4 baris
    y_row1 = int(0.58 * 10000)   # 5800 - baris 1 (1,2,3)
    y_row2 = int(0.67 * 10000)   # 6700 - baris 2 (4,5,6)
    y_row3 = int(0.76 * 10000)   # 7600 - baris 3 (7,8,9)
    y_row4 = int(0.85 * 10000)   # 8500 - baris 4 (DEL,0)
    
    return {
        '1': (x_left, y_row1),
        '2': (x_center, y_row1),
        '3': (x_right, y_row1),
        '4': (x_left, y_row2),
        '5': (x_center, y_row2),
        '6': (x_right, y_row2),
        '7': (x_left, y_row3),
        '8': (x_center, y_row3),
        '9': (x_right, y_row3),
        '0': (x_center, y_row4),
        'D': (x_left, y_row4),    # Delete
        'OK': (x_center, int(0.55 * 10000)),  # OK popup
        'WAKE': (x_center, int(0.50 * 10000)),  # Wake screen
    }


